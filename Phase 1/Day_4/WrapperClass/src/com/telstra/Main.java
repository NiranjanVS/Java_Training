package com.telstra;

public class Main {

	public static void main(String[] args) {
		
		//Boxing :Conversion of Primitive to Wrapper class
		int i = 10;
		Integer I = Integer.valueOf(i);
		System.out.println(I);
		
		//Un boxing : Conversion of Wrapper class to Primitive type
		
		//Auto boxing
		I = 100;
		String S = Integer.toString(1290);
		
	}

}
